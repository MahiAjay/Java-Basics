public class _06math {
    public static void main(String[]args) {
       //basic math class
        int num1 = 3, num2 = 5;
        System.out.println(Math.max(num1, num2));//gives the maximum number
        System.out.println(Math.min(num1, num2));//gives the minimum number 
        System.out.println(Math.sqrt(36));//gives the square root
        System.out.println(Math.abs(-40));// changes the number to positive
        System.out.println(Math.abs(40));
        System.out.println(Math.random());//generates random number between 0 and 1
        System.out.println(4+(8-4)*Math.random());// will generate number only between 4 and 8

        //search math class functions on internet for more details



    }
}
